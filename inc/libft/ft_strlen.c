#include <unistd.h>
#include <stdio.h>
#include "libft.h"

/* Returns the size of the string */
size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
	{
		i++;
	}
	return (i);
}
